/**
 * File Operations 프롬프트 컴포넌트
 * 파일 작업 형식 및 규칙
 */

export function getFileOperationsRules(): string {
  return `파일 작업 형식 (이해할 수 있는 **유일한 형식**입니다. 이 규칙을 지키지 않으면 파일 작업이 수행되지 않습니다):
- **새 파일**: 반드시 한 줄 지시어 + 전체 코드 블록
  - 형식: \`새 파일: [파일경로]\`
  - 예시: \`새 파일: package.json\` + 아래에 전체 내용을 포함한 코드 블록
- **수정 파일**: 반드시 한 줄 지시어 + 수정 후 전체 코드 블록
  - 형식: \`수정 파일: [파일경로]\`
  - 예시: \`수정 파일: src/App.tsx\` + 아래에 해당 파일의 전체 내용을 포함한 코드 블록
- **삭제 파일**: 반드시 한 줄 지시어만 사용
  - 형식: \`삭제 파일: [파일경로]\`
- **마크다운(.md)**: 코드 블록 없이, 지시어 뒤에 **마크다운 본문 전체**를 직접 포함
  - 형식: \`새 파일: [파일경로].md\` 또는 \`수정 파일: [파일경로].md\` 한 줄 + 바로 아래에 마크다운 전체 내용

** 잘못된 형식 (절대 사용 금지 – 파일을 인식하지 못합니다)**:
- 단순 목록이나 번호 목록:
  - \`1. package.json\`, \`2. tsconfig.json\` 처럼 지시어 없이 파일명만 나열
- 콜론만 붙인 형식:
  - \`package.json:\`, \`src/main.tsx:\` 처럼 "새 파일:" / "수정 파일:" 접두사가 없는 경우
- 설명 문장에 섞인 파일명:
  - "다음 파일들을 생성합니다: package.json, tsconfig.json, src/main.tsx" 와 같이 지시어 없이 기술

**JSON 파일 주석 금지**:
- **package.json, tsconfig.json, .eslintrc.json 등 모든 JSON 파일에는 주석을 절대 포함하지 마세요.**
- JSON 표준은 주석을 지원하지 않습니다. 주석이 포함되면 파싱 오류가 발생합니다.

**tsconfig.json 특수 규칙**:
- **tsconfig.json에 "references" 필드는 절대 추가하지 마세요.**
- "references": [{ "path": "./tsconfig.node.json" }] 같은 필드를 추가하지 마세요.
- 단일 tsconfig.json 파일만 사용하고, references를 통한 프로젝트 참조는 사용하지 마세요.`;
}

