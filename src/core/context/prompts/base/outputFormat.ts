/**
 * Output Format 프롬프트 컴포넌트
 * 기본 출력 형식 가이드
 */

export function getDefaultOutputFormat(): string {
    return `1. **작업 요약**: 수행할 작업의 개요를 먼저 작성
2. **파일 작업 ( 인식할 수 있는 **유일한 형식**입니다)**: 각 파일마다 반드시 다음 형식을 사용:
   - 새 파일: \`새 파일: [파일경로]\`
   - 수정 파일: \`수정 파일: [파일경로]\`
   - 삭제 파일: \`삭제 파일: [파일경로]\`
   - 마크다운(.md) 파일: \`새 파일: [파일경로].md\` 또는 \`수정 파일: [파일경로].md\` 한 줄 + 바로 아래에 마크다운 전체 본문
   - **주의 1**: \`1. package.json\`, \`package.json:\` 처럼 지시어 없이 파일명만 나열하면 aidev-ide가 파일을 생성/수정/삭제할 수 없습니다.
   - **주의 2**: \`새 파일: [파일경로]\` / \`수정 파일: [파일경로]\` / \`삭제 파일: [파일경로]\` 지시어는 **반드시 한 줄 평문으로 작성**해야 합니다.
     - \`**새 파일: design.md**\`, \`- 새 파일: design.md\` 처럼 볼드나 목록 기호로 감싸지 마세요. (이 경우 도구가 파일 작업으로 인식하지 못할 수 있습니다.)
3. **코드**: 각 파일 지시어 바로 아래에 마크다운 코드 블록으로 해당 파일의 **전체 내용** 제공
4. **설명**: 변경사항에 대한 상세 설명
5. **테스트**: 동작 확인 방법`;
}

