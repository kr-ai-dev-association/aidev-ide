/**
 * Code Work 작업 타입 프롬프트 컴포넌트
 * code_work 작업에 대한 특화 프롬프트
 */

export function getCodeWorkPrompt(): string {
  return `**코드 작성 작업 (code_work) 특화 규칙:**
- 소스 코드 파일(.js, .ts, .py, .java, .go, .rs 등) 생성/수정/삭제만 수행.
- **절대로 쉘 스크립트(.sh, .bat, .ps1)나 빌드 스크립트를 생성하지 마세요.**
- **절대로 터미널 명령어 코드 블록을 생성하지 마세요.**
- 프로그래밍 언어의 소스 코드 파일만 작성하세요.

**⚠️ 프로젝트 생성 작업 (매우 중요 - 절대 금지 사항 - 이 규칙을 위반하면 심각한 오류 발생)**:
  * **필수 사항 (반드시 준수해야 함 - 이것을 지키지 않으면 작업이 실패합니다)**:
    - 프로젝트 구조 파일(pom.xml, build.gradle, package.json, vite.config.ts, tsconfig.json 등)과 소스 코드 파일을 "새 파일: [파일경로]" 형식으로 반드시 생성하세요.
    - 모든 필요한 파일을 "새 파일:" 지시어로 생성하세요. 이것은 선택 사항이 아닌 필수입니다.
    - 오직 "새 파일: [파일경로]" 지시어만 사용하세요. 다른 방법은 절대 사용하지 마세요.
    - 예시: "새 파일: pom.xml" + 코드 블록, "새 파일: src/main/java/com/example/App.java" + 코드 블록, "새 파일: package.json" + 코드 블록
  * **절대 금지 사항 (이 규칙을 위반하면 심각한 오류 발생 - 절대 사용하지 마세요)**:
    - 터미널 명령어 코드 블록(\`\`\`bash) 생성 절대 금지 - 이것을 사용하면 심각한 오류 발생
    - cat <<'EOF' > file 같은 heredoc 명령어 사용 절대 금지 - 이것을 사용하면 심각한 오류 발생
    - mkdir -p, cat, echo 같은 파일 생성 명령어 사용 절대 금지 - 이것을 사용하면 심각한 오류 발생
    - if ! command -v brew 같은 조건문이나 도구 설치 명령어 포함 절대 금지
    - npm install, pnpm install, yarn install 같은 패키지 설치 명령어 포함 절대 금지 - 파일 생성 후 별도로 실행되므로 포함하지 마세요
    - brew install, apt install 같은 패키지 매니저 명령어 포함 절대 금지
  - 빌드나 실행은 소스 파일 생성 후 별도로 처리됩니다.
  - **중요: 프로젝트 생성 요청에 대해 터미널 명령어만 반환하면 작업이 실패합니다. 반드시 파일을 생성하세요.**`;
}

